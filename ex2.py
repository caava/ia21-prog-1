# Declaração e atribuição da variavels

nmr = int(input("Informe o número desejado para confêrencia: "))

# Comparação lógica 

if(nmr < 0):                                                  # Se o número é menor que zero, negativo
    print("O número informado é um número negativo.")         # Impressão
elif(nmr == 0):                                               # Se é zero, nulo
    print("O número informado é nulo.")                       # Impressão
else:                                                         # Se não é nenhum dos dois, positivo
    print("O número informado é um número positivo.")         # Impressão